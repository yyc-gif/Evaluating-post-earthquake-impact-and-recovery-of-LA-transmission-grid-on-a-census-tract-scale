def load_shapefile():
    """
    Load the tract shapefile and standardize the tract id column name to 'tract_id'.
    Returns:
        GeoDataFrame or None
    """
    if not os.path.exists(SHAPEFILE_PATH):
        print(f"  [GIS Skip] Shapefile not found at {SHAPEFILE_PATH}")
        return None

    try:
        gdf = gpd.read_file(SHAPEFILE_PATH)

        possible_cols = ["GEOID", "GEOID10", "GEOID20", "TRACTCE", "TRACT", "geoid"]
        for c in possible_cols:
            if c in gdf.columns:
                gdf["tract_id"] = gdf[c].astype(str).str.strip()
                break

        if "tract_id" not in gdf.columns:
            print("  [GIS Warning] Could not identify Tract ID column in shapefile.")
            return None

        return gdf

    except Exception as e:
        print(f"  [GIS Error] Failed to load shapefile: {e}")
        return None

def plot_map(
    gdf,
    data_series,
    title: str,
    output_dir: str,
    filename: str,
    cmap: str = "OrRd",
    label: str = "Value",
    vmin=None,
    vmax=None,
    cmap_low: float = 0.1,
    cmap_high: float = 1.0,
    figure_role: str = "PANEL_MAP_TALL",
):
    """
    Render a tract-level choropleth using the shared publication map style.

    The routine performs robust tract-ID alignment between the supplied
    GeoDataFrame and metric series, supports externally imposed color limits for
    cross-panel comparability, and exports the final figure at the configured
    manuscript size with consistent colorbar and typography settings.
    """
    if gdf is None:
        return

    # ---- Prepare data ----
    data_df = data_series.to_frame(name="val")
    data_df.index = (
        data_df.index.astype(str)
        .str.split(".").str[0]
        .str.strip()
    )

    gdf["tract_id"] = (
        gdf["tract_id"].astype(str)
        .str.split(".").str[0]
        .str.strip()
    )

    # ---- Matching strategies ----
    matched_gdf = None
    best_match_count = 0

    strategies = [
        ("Direct", lambda s: s),
        ("CSV zfill 11", lambda s: s.str.zfill(11)),
        ("Shapefile zfill 11", None),
        ("Suffix 6", lambda s: s.str[-6:]),
    ]

    for name, func in strategies:
        temp_data = data_df.copy()
        temp_gdf = gdf.copy()

        if name == "Shapefile zfill 11":
            temp_gdf["tract_id"] = temp_gdf["tract_id"].str.zfill(11)
        elif func:
            temp_data.index = func(temp_data.index)

        merged = temp_gdf.merge(
            temp_data,
            left_on="tract_id",
            right_index=True,
            how="inner",
        )

        count = len(merged)
        if count > best_match_count:
            best_match_count = count
            matched_gdf = merged
            print(f"   > Strategy '{name}' matched: {count} tracts")

    if best_match_count == 0 or matched_gdf is None:
        print("   ❌ [GIS Error] ALL MATCHING STRATEGIES FAILED.")
        return

    print(f"   ✅ Final Map Area: {best_match_count} tracts.")

    # ---- Plot ----
    fig, ax = plt.subplots(figsize=get_figsize(figure_role))

    # vmin/vmax based on finite values only
    data_vals = matched_gdf["val"].astype(float)
    data_vals_valid = data_vals[np.isfinite(data_vals)]
    if vmin is None:
        vmin = float(data_vals_valid.min())
    if vmax is None:
        vmax = float(data_vals_valid.max())

    # Truncated colormap to avoid near-white low end
    base_cmap = cmap if isinstance(cmap, mcolors.Colormap) else plt.colormaps.get_cmap(cmap)
    cmap_name = getattr(base_cmap, "name", str(cmap))
    new_colors = base_cmap(np.linspace(cmap_low, cmap_high, 256))
    truncated_cmap = mcolors.LinearSegmentedColormap.from_list(
        f"{cmap_name}_trunc_{cmap_low}_{cmap_high}",
        new_colors,
    )

    matched_gdf.plot(
        column="val",
        ax=ax,
        legend=False,
        cmap=truncated_cmap,
        vmin=vmin,
        vmax=vmax,
        missing_kwds={
            "color": "lightgrey",
            "edgecolor": "none",
            "hatch": "///",
        },
    )

    # Colorbar
    divider = make_axes_locatable(ax)
    cax = divider.append_axes("right", size="5%", pad=0.1)

    sm = plt.cm.ScalarMappable(
        cmap=truncated_cmap,
        norm=plt.Normalize(vmin=vmin, vmax=vmax),
    )
    sm._A = []  # dummy for ScalarMappable

    ticks = np.linspace(vmin, vmax, 5)
    cbar = fig.colorbar(sm, cax=cax, ticks=ticks)
    cbar.ax.set_yticklabels([f"{t:.2f}" for t in ticks])
    style_colorbar(cbar, label=label)

    style_axis(ax, title=title)
    ax.axis("off")

    # Save
    save_plot(fig, output_dir, filename)


