    # 5) Plot
    # -------------------------------------------------------------------------
    fig, ax = plt.subplots(
        figsize=get_figsize(
            "PANEL_ASYM_RIGHT",
            width_cm=STAGE2_TOP10_MAP_LAYOUT["width_cm"],
            height_cm=STAGE2_TOP10_MAP_LAYOUT["height_cm"],
        )
    )

    # Tract boundaries basemap
    if tracts is not None and not tracts.empty:
        tracts.boundary.plot(
            ax=ax,
            color="#d0d0d0",
            linewidth=0.5,
            zorder=0,
        )


    # City boundary outline (to make the city edge visible)
    if "CITY_BOUNDARY_SHP" in globals() and os.path.exists(CITY_BOUNDARY_SHP):
        try:
            city_bnd = gpd.read_file(CITY_BOUNDARY_SHP)
            if city_bnd.crs is None:
                minx, miny, maxx, maxy = city_bnd.total_bounds
                if -180 <= minx <= 180 and -90 <= miny <= 90:
                    city_bnd = city_bnd.set_crs(epsg=4326)
                else:
                    print(f"   [GIS Error] City Boundary CRS missing & huge coords. Cannot assume 3857 safely.")
                    raise ValueError("City Boundary shapefile missing CRS (.prj).")
            city_bnd = city_bnd.to_crs(epsg=4326)
            city_bnd.plot(
                ax=ax,
                facecolor="none",
                edgecolor="#7a7a7a",
                linewidth=1.2,
                zorder=0.2,
            )
        except Exception as e:
            print(f"  [Stage 2] City boundary plot skipped: {e}")

    # All substations
    all_subs_gdf.plot(
        ax=ax,
        marker="o",
        facecolor="#6f6f6f",
        edgecolor="none",
        markersize=14,
        alpha=0.42,
        label="Substations",
        zorder=1,
    )

    # Top10 substations
    if not top10_gdf.empty:
        top10_gdf.plot(
            ax=ax,
            marker="*",
            markersize=78,
            facecolor="#f4a300",
            edgecolor="black",
            linewidth=0.8,
            label="Top 10 by impact centrality",
            zorder=2,
        )

        # Rank annotations
        # Keep labels predominantly on the same side of each marker and
        # manually stagger the densest cluster to avoid overlap.
        rank_offsets = {
            1: (6, 8),
            2: (10, 13),
            3: (12, 3),
            4: (8, -12),
            5: (12, -8),
            6: (6, 8),
            7: (8, 5),
            8: (8, -18),
            9: (-9, 10),
            10: (8, -10),
        }
        for _, row in top10_gdf.iterrows():
            if pd.notnull(row.geometry):
                rank_val = int(row["rank"])
                x_off, y_off = rank_offsets.get(rank_val, (7, 5))
                ax.annotate(
                    text=str(rank_val),
                    xy=(row.geometry.x, row.geometry.y),
                    xytext=(x_off, y_off),
                    textcoords="offset points",
                    fontsize=FS_ANNOTATION,
                    fontweight="semibold",
                    color="#222222",
                    ha="left" if x_off >= 0 else "right",
                    va="bottom" if y_off >= 0 else "top",
                    bbox=dict(
                        boxstyle="round,pad=0.08",
                        facecolor="white",
                        edgecolor="none",
                        alpha=0.78,
                    ),
                )

    # -------------------------------------------------------------------------
    # 6) Set extent (prefer city boundary bbox, then substations, then tracts)
    # -------------------------------------------------------------------------
    bounds_found = False

    if "CITY_BOUNDARY_SHP" in globals() and os.path.exists(CITY_BOUNDARY_SHP):
        try:
            city = gpd.read_file(CITY_BOUNDARY_SHP)
            if city.crs is None:
                mx, my, Mxx, Mxy = city.total_bounds
                if -180 <= mx <= 180 and -90 <= my <= 90:
                    city = city.set_crs(epsg=4326)
                else:
                    print("   [GIS Warning] City Boundary CRS missing, skipping bounds check.")
                    raise ValueError("Missing CRS")
            city = city.to_crs(epsg=4326)
            minx, miny, maxx, maxy = city.total_bounds
            bounds_found = True
        except Exception:
            pass
    elif not all_subs_gdf.empty:
        minx, miny, maxx, maxy = all_subs_gdf.total_bounds
        bounds_found = True
    elif tracts is not None and not tracts.empty:
        minx, miny, maxx, maxy = tracts.total_bounds
        bounds_found = True

    if not bounds_found:
        print("[vis_stage2_top10_map] No geometry bounds found.")
        plt.close(fig)
        return

    dx = (maxx - minx) * 0.05
    dy = (maxy - miny) * 0.05
