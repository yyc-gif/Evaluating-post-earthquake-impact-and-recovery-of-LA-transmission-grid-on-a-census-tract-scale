def _safe_lcc_fraction(df: pd.DataFrame):
    """
    Prefer lcc_fraction. If only lcc_size exists, normalize to [0,1] for plotting.
    This avoids baseline plotting failure when Stage 3 schema differs.
    """
    if "lcc_fraction" in df.columns:
        return df["lcc_fraction"]

    if "lcc_size" in df.columns:
        mx = float(df["lcc_size"].max())
        if mx > 0:
            return df["lcc_size"] / mx
        return df["lcc_size"] * 0.0

    return None

def plot_gantt_chart(
    df: pd.DataFrame,
    output_dir: str,
    title_suffix: str,
    max_crews: int = 50,
    stage_label: str | None = None,
) -> None:
    """
    Generic function to plot Crew Schedule Gantt Charts.
    - Red bars: Travel time
    - Blue bars: Repair duration
    """
    if df.empty:
        print(f"  [Gantt] Warning: DataFrame for {title_suffix} is empty. Skipping.")
        return

    # 1. Filter crews if there are too many
    all_crews = sorted(df["Crew_ID"].unique())
    if len(all_crews) > max_crews:
        print(f"  [Gantt] Too many crews ({len(all_crews)}), plotting first {max_crews} only.")
        subset_crews = all_crews[:max_crews]
        df = df[df["Crew_ID"].isin(subset_crews)].copy()

    # Dynamic figure height
    fig_height_cm = max(PANEL_GANTT["height_cm"], 0.55 * len(df["Crew_ID"].unique()) + 5.0)
    fig, ax = plt.subplots(
        figsize=get_figsize("PANEL_GANTT", height_cm=fig_height_cm)
    )

    # 2. Map Crew IDs to Y-axis positions
    crews = sorted(df["Crew_ID"].unique())
    y_map = {c: i for i, c in enumerate(crews)}

    def _display_crew_label(crew_id):
        """Format crew identifiers as human-readable labels for the Gantt y-axis."""
        try:
            crew_num = int(float(crew_id))
            return f"Crew {crew_num + 1}" if crew_num >= 0 else f"Crew {crew_num}"
        except Exception:
            return f"Crew {crew_id}"

    # 3. Plot Bars
    added_label_repair = False
    added_label_travel = False

    for _, row in df.iterrows():
        y = y_map[row["Crew_ID"]]
        repair_start = row["Start_Time"]
        repair_dur = row["Duration"]
        travel_dur = row["Travel_Time"]
        travel_start = repair_start - travel_dur

        # Plot Travel (Red)
        if travel_dur > 0:
            lbl = "Travel Time" if not added_label_travel else ""
            ax.barh(
                y, travel_dur, left=travel_start, 
                color="#ff9999", edgecolor="none", height=0.6, label=lbl
            )
            added_label_travel = True

        # Plot Repair (Blue)
        lbl = "Repair Duration" if not added_label_repair else ""
        ax.barh(
            y, repair_dur, left=repair_start, 
            color="#66b3ff", edgecolor="black", height=0.6, label=lbl
        )
        added_label_repair = True

    # 4. Formatting
    display_title = (
        str(title_suffix)
        .replace("Stage5_GA_", "")
        .replace("Stage4_", "")
        .replace("_", " ")
    )

    ax.set_yticks(list(y_map.values()))
    ax.set_yticklabels([_display_crew_label(c) for c in crews], fontsize=FS_TICK)
    ax.invert_yaxis()
    style_axis(
        ax,
        title=display_title,
        xlabel="Time (hours) since event",
        ylabel="Maintenance crews",
    )
    
    if not df.empty:
        max_end_time = (df["Start_Time"] + df["Duration"]).max()
        if pd.notna(max_end_time) and max_end_time > 0:
            ax.set_xlim(0, max_end_time * 1.05) 

    ax.grid(True, axis="x", linestyle="--", alpha=0.5)
    legend = ax.legend(
        loc="upper left", 
        bbox_to_anchor=(1.01, 1), 
        borderaxespad=0,
        frameon=True,
    )
    format_legend(legend)

    # 5. Save
    clean_name = (
        str(title_suffix)
        .replace("Stage5_GA_", "")
        .replace("Stage4_", "")
    )
    safe_name = clean_name.replace(" ", "_").replace("/", "-").replace(":", "")
    resolved_stage_label = stage_label
    if not resolved_stage_label:
        match = re.search(r"stage\s*([0-9]+)", os.path.basename(str(output_dir)), flags=re.IGNORECASE)
        if match:
            resolved_stage_label = f"stage{match.group(1)}"
        else:
            resolved_stage_label = "stage"
    filename = f"vis_{resolved_stage_label}_gantt_{safe_name}.png"
    plt.tight_layout()
    save_plot(fig, output_dir, filename)


